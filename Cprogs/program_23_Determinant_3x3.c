#include <stdio.h>

double det3(double A[3][3]){
    return A[0][0]*(A[1][1]*A[2][2]-A[1][2]*A[2][1])
         - A[0][1]*(A[1][0]*A[2][2]-A[1][2]*A[2][0])
         + A[0][2]*(A[1][0]*A[2][1]-A[1][1]*A[2][0]);
}

int main(){
    double A[3][3] = {{1.0,2.0,3.0},{0.0,1.0,4.0},{5.0,6.0,0.0}};
    printf("%.6lf\n", det3(A));
    return 0;
}